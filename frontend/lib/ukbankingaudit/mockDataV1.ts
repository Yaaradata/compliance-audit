const ANCHOR_DATE = new Date('2026-04-30T18:00:00Z');
const DAY_MS = 86400000;
const WEEK_MS = 7 * DAY_MS;
const daysAgo = (n) => new Date(ANCHOR_DATE.getTime() - n * DAY_MS).toISOString();
const weeksAgo = (n) => new Date(ANCHOR_DATE.getTime() - n * WEEK_MS).toISOString();
const dateOnly = (n) => daysAgo(n).slice(0, 10);
const wobble = (a, i) => Math.sin(i * 1.7) * a * 0.5 + Math.cos(i * 0.9) * a * 0.3;
const trendingSeries = (start, end, points = 13, noise = 1.2) => {
  const out = [];
  for (let i = 0; i < points; i++) {
    const t = i / (points - 1);
    out.push(Math.round((start + (end - start) * t + wobble(noise, i)) * 10) / 10);
  }
  return out;
};
const stableSeries = (centre, points = 13, noise = 0.8) => {
  const out = [];
  for (let i = 0; i < points; i++) out.push(Math.round((centre + wobble(noise, i)) * 10) / 10);
  return out;
};

